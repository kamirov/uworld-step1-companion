function normalize(text) {
  return text
    .toLowerCase()
    .replace(/[βγδεζηθικλμνξοπρστυφχψω]/g, (c) => {
      const map = { β: "b", γ: "g", δ: "d", ε: "e" };
      return map[c] ?? c;
    })
    .replace(/[''`]/g, "'")
    .replace(/[^a-z0-9+'\s-]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function matchSuffix(word) {
  for (const [suffix, meaning] of Object.entries(SUFFIXES)) {
    if (word.endsWith(suffix) && word.length > suffix.length + 2) {
      return meaning;
    }
  }
  return null;
}

function wordMatchesRoot(word, root) {
  if (word === root) return true;
  if (word.length < root.length) return false;
  if (!word.includes(root)) return false;
  const idx = word.indexOf(root);
  const before = word[idx - 1];
  const after = word[idx + root.length];
  const boundary = (ch) => !ch || !/[a-z]/.test(ch);
  return boundary(before) && boundary(after);
}

function matchRoots(text) {
  const normalized = normalize(text);
  const compact = normalized.replace(/\s+/g, " ");
  const words = compact.split(/[\s-]+/).filter(Boolean);
  const parts = [];

  for (const word of words) {
    for (const [root, meaning] of ROOTS) {
      if (wordMatchesRoot(word, root)) {
        parts.push(meaning);
        break;
      }
    }
    const suffix = matchSuffix(word);
    if (suffix && !parts.includes(suffix)) parts.push(suffix);
  }

  if (parts.length > 0) {
    const unique = [...new Set(parts)].slice(0, 2);
    return unique.join("; ");
  }

  for (const [root, meaning] of ROOTS) {
    if (compact.includes(root) && compact.split(/[\s-]+/).some((w) => wordMatchesRoot(w, root))) {
      return meaning;
    }
  }

  return null;
}

function isEponym(name) {
  return /\b(syndrome|disease|sign|triad|tetrad|law|rule|criteria|phenomenon|effect|reflex|maneuver|test|classification|scale|score|type)\b/i.test(
    name,
  );
}

function isAbbreviation(name) {
  const stripped = name.replace(/[()]/g, "").trim();
  return /^[A-Z0-9][A-Za-z0-9+\-/ ]{0,20}$/.test(stripped) && stripped.length <= 12;
}

export function generateEtymology(id, name) {
  if (ID_OVERRIDES[id]) return ID_OVERRIDES[id];

  const rootMatch = matchRoots(`${id} ${name}`);
  if (rootMatch) return rootMatch;

  const normName = normalize(name);
  const normId = normalize(id.replace(/-/g, " "));

  if (isAbbreviation(name.split("(")[0].trim())) {
    const expanded = matchRoots(normId);
    if (expanded) return expanded;
    return `Abbreviation: ${name.split("(")[0].trim()}`;
  }

  if (isEponym(name)) {
    const eponym = name.split(/\s+(syndrome|disease|sign|triad|tetrad|law|rule|criteria|phenomenon|effect|reflex|maneuver|test)/i)[0];
    return `Named for ${eponym.trim()}`;
  }

  if (/^\d/.test(name) || /^[sS]\d/.test(name)) {
    return "Numbered clinical designation";
  }

  if (name.includes("(") && name.includes(")")) {
    const before = name.split("(")[0].trim();
    const inside = name.match(/\(([^)]+)\)/)?.[1];
    const inner = matchRoots(inside ?? "");
    const outer = matchRoots(before);
    if (outer && inner) return `${outer}; ${inside}`;
    if (outer) return outer;
    if (inner) return `Abbreviation: ${inside}`;
  }

  if (/virus$/i.test(name) || normId.includes("virus")) {
    return "Latin virus (poison) + pathogen name";
  }

  if (/bacterium|coccus|bacillus|bacilli/i.test(name)) {
    return matchRoots(name) ?? "Greek bakterion (rod) or kokkos (berry)";
  }

  if (/-(mab|nib|vir|cillin|mycin|pril|olol|sartan|statin|azole|pine)$/i.test(name)) {
    return matchRoots(name) ?? "Pharmaceutical name suffix";
  }

  return `From ${normName || normId}`;
}
